在Lua中，定义函数非常简单。你可以使用function关键字后跟函数名和参数列表（参数之间用逗号分隔，如果函数不接受任何参数，则参数列表为空），然后是一个代码块（用大括号{}包围），该代码块包含了函数被调用时执行的代码。函数定义完成后，你可以通过函数名加上圆括号（内部可以包含传递给函数的参数）来调用它。
下面是一个简单的例子，展示了如何在Lua中定义和调用函数：
l-- 定义一个函数，该函数接受两个参数并返回它们的和 
function add(a, b)     
return a + b  
end   
 -- 调用函数并打印结果  
print(add(5, 3))  -- 输出: 8
在Lua中，你也可以使用匿名函数（也称为lambda函数或闭包），这些函数可以在需要时定义并立即使用，或者赋值给变量供后续使用。匿名函数通常与Lua的表（table）和更高级的功能（如高阶函数）一起使用。
下面是一个使用匿名函数的例子：
-- 定义一个匿名函数并将其赋值给变量add  
add = function(a, b)      
return a + b 
end   
 -- 调用函数并打印结果  
print(add(5, 3))  -- 输出: 8
在Lua中，你还可以使用所谓的“点语法”或“冒号语法”来定义对象的方法，但本质上它们仍然是函数。不过，这涉及到Lua中的面向对象编程概念，超出了基本函数定义的范畴。
Lua的函数是一等公民（first-class citizens），这意味着它们可以被赋值给变量、作为参数传递给其他函数、以及从其他函数返回。这使得Lua在函数式编程范式中非常灵活和强大。