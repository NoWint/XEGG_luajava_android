常用颜色的六位色号（十六进制颜色代码）通常以“#”开头，后跟六个十六进制数字，分别代表红色、绿色和蓝色的强度，每个颜色的取值范围是00-FF。以下是一些常用颜色的六位色号示例：

| 颜色名称 | 十六进制颜色代码 |
| --- | --- |
| 白色 | #FFFFFF |
| 红色 | #FF0000 |
| 绿色 | #00FF00 |
| 蓝色 | #0000FF |
| 黄色 | #FFFF00 |
| 黑色 | #000000 |
| 牡丹红 | #FF00FF |
| 青色 | #00FFFF |
| 橙色 | #FFA500（或#FF7F00，具体取决于橙色的深浅） |
| 紫色 | #800080（或#FF00FF，取决于紫色的深浅和色调） |
| 灰色 | #808080（或#C0C0C0，取决于灰色的深浅） |
| 棕色 | #A52A2A（或#8B4513，取决于棕色的深浅和色调） |
| 粉色 | #FFC0CB（浅粉色） |
| 深蓝色 | #00008B |
| 深绿色 | #006400 |
| 珊瑚色 | #FF7F50 |
| 浅灰色 | #A9A9A9 |
| 天蓝色 | #87CEEB |
| 银色 | #C0C0C0（或更浅的灰色，如#E0E0E0，取决于银色的亮度） |
| 金色 | #FFD700（或更深的金色，如#FFB800，取决于金色的深浅） |

请注意，由于颜色的多样性，上述列表并不包含所有可能的颜色及其色号。此外，对于某些颜色，可能存在多个相近的色号，具体取决于颜色的深浅、色调和饱和度。

在设计或编程中，选择适当的颜色代码对于确保颜色的一致性和准确性至关重要。因此，建议在使用颜色代码时仔细核对，并根据需要调整颜色的深浅和色调。