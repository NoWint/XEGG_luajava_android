在Android开发中，match_parent和wrap_content是两个常见的布局参数，用于指定控件（View）的宽度（layout_width）和高度（layout_height）。然而，您提到的wrap_concert似乎是一个拼写错误，我猜测您可能想表达的是wrap_content。以下是对这两个术语的详细解释：
1. match_parent

定义：match_parent是一个布局参数值，表示控件应该扩展以匹配其父控件的完整宽度或高度（取决于它是被用作宽度还是高度的值）。这意味着，如果控件的layout_width或layout_height被设置为match_parent，那么控件的大小将与其父控件的相应维度相匹配。
用途：当您希望控件占据其父控件的全部可用空间时，使用match_parent是非常有用的。然而，过度使用可能会导致布局嵌套过深，影响性能。
注意：在早期的Android版本中，match_parent被称为fill_parent，但match_parent是官方推荐的用法。

2. wrap_content

定义：wrap_content是另一个布局参数值，它指示控件的大小应该足够大，以刚好包含其内容。这意味着控件会根据其内容（如文本长度、图片大小等）自动调整其大小。
用途：当您希望控件根据其内容动态调整大小时，使用wrap_content是非常适合的。这有助于创建更加灵活和响应式的用户界面。
示例：在一个TextView中，如果layout_width和layout_height都被设置为wrap_content，那么TextView的宽度和高度将仅足够显示其内部的文本。

总结

match_parent用于使控件与其父控件的相应维度相匹配。
wrap_content用于使控件根据其内容自动调整大小。

